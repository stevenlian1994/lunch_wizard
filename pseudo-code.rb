function(offer plans for lunch)
    -check event locations for events closest to lunch time
        -if 0 locations: ask user if they want help grabbing lunch, and then offer a 5 question survey of the plan for the day
        -if 1 location: make a list of eateries near that location
        -if 2 locations: make a list of eateries in between the two locations and offer these options to user